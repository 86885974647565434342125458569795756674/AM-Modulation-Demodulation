function am()
Ts=0.01;
t=0:Ts:10;%时间
w=10;
mp=4*sin(w.*t+7)+8*cos(w.*t+12);%调制信号
%时域
figure;
plot(t,mp);
xlabel('Time');
ylabel('Amplitude');
%频域
figure;
MP=fft(mp);
fs=1/Ts;
f=(0:length(MP)-1)*fs/length(MP);
plot(f,abs(MP));
xlabel('Frequency');
ylabel('Manitude');

%调制
m=mp+max(mp);
w0=50.*w;
c=cos(w0.*t);%载波
s=c.*m;%已调信号
%时域
figure;
plot(t,s);
xlabel('Time');
ylabel('Amplitude');
%频域
figure;
S=fft(s);
plot(f,abs(S));
xlabel('Frequency');
ylabel('Manitude');

%相干解调
z=s.*c;
%低通滤波器
z=lowpass(z,w/2/pi,fs);
s=2.*z-1;
%时域
figure;
plot(t,z);
xlabel('Time');
ylabel('Amplitude');
%频域
figure;
S=fft(s);
plot(f,abs(S));
xlabel('Frequency');
ylabel('Manitude');
